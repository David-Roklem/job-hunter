/**
 * Промпт для извлечения зарплаты из свободного текста поста Telegram-канала.
 *
 * Посты каналов вакансий пишут зарплату вразнобой: «вилка 250–350к», «$120k»,
 * «от 3000 EUR», «зп по итогам собеса», «не указана». Регэкспы ловят только
 * простые случаи ($, k, диапазоны); всё остальное — этот LLM-промпт.
 *
 * Возвращает СТРОГО JSON: {"from":N,"to":N,"currency":"RUB|USD|EUR"} либо null.
 * Низкая температура (0) для детерминированности.
 */
import type { ChatMessage } from "../types";

/** Максимальная длина текста поста в промпте (защита от контекста). */
const MAX_TEXT_CHARS = 2000;

/** Собирает system + user сообщения для извлечения зарплаты. */
export function buildSalaryMessages(text: string): ChatMessage[] {
  const excerpt = text.length > MAX_TEXT_CHARS ? `${text.slice(0, MAX_TEXT_CHARS)}…` : text;
  return [
    { role: "system", content: SYSTEM_PROMPT },
    { role: "user", content: `Текст поста:\n"""\n${excerpt}\n"""\n\nИзвлеки зарплату.` },
  ];
}

const SYSTEM_PROMPT = [
  "Извлеки зарплату из текста поста вакансии.",
  "Верни СТРОГО JSON без markdown-обёртки и пояснений:",
  '  {"from": <число>, "to": <число>, "currency": "RUB|USD|EUR"}',
  "Правила:",
  "- Суммы — годовые/месячные gross в указанной валюте; множитель «к»/«k» = ×1000.",
  "- Диапазон «250–350к» → {\"from\":250000,\"to\":350000,\"currency\":\"RUB\"}.",
  "- Если указана только нижняя граница («от 200к») → только from.",
  "- Если верхняя («до 400к») → только to.",
  "- Валюта: «$»→USD, «€»/EUR→EUR, «руб»/₽/RUB→RUB, по умолчанию RUB.",
  "- Если зарплата не указана / «по договорённости» / «договорная» → верни null.",
  "- Никаких других полей, никакого текста кроме JSON или null.",
].join("\n");
